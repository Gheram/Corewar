#include "libft.h"

intmax_t			ft_latoi(const char *str)
{
	intmax_t		nb;
	int				p;

	nb = 0;
	p = 1;
	while ((*str >= 0 && *str <= 26) || (*str >= 28 && *str <= 32))
		str++;
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		p = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + (*str - '0');
		str++;
	}
	return (nb * p);
}
