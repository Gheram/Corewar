#include "vm.h"

void	fill_win(WINDOW *left_win)
{
	int i;
	int j;

	i = -1;
	while (++i < 64)
	{
		j = -1;
		while (++j < 64)
		{
			mvwprintw(left_win, 1 + i, 3 + (j * 3), "00");
		}
	}
}
