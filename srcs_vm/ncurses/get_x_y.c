#include "vm.h"

int	get_x(unsigned int x)
{
	x = x % MEM_SIZE;
	x = x % 64;
	return (x * 3 + 3);
}

int	get_y(unsigned int y)
{
	y = y % MEM_SIZE;
	y = y / 64;
	return (y + 1);
}
